  BEGIN
    INSERT INTO BOLETA (ID_BOLETA, RUT_CLIENTE, ID_FORMA_PAGO, ID_ENTREGA, TOTAL) VALUES (idd,rutt,pago,entrega,total);
    COMMIT;
  END;
  PROCEDURE actualizar(rutt in VARCHAR2, pago in NUMBER, entrega in NUMBER, total in NUMBER, idd in NUMBER) IS
  BEGIN
    UPDATE BOLETA SET RUT_CLIENTE = rutt, ID_FORMA_PAGO = pago, ID_ENTREGA = entrega, TOTAL = total WHERE ID_BOLETA = idd;
    COMMIT;
  END;
  PROCEDURE eliminar(idd in NUMBER, resp out NUMBER) IS
  bbb BOLETA.ID_BOLETA%TYPE;
  BEGIN
    SELECT B.ID_BOLETA INTO bbb FROM BOLETA B WHERE B.ID_BOLETA = idd;
    DELETE FROM BOLETA WHERE ID_BOLETA = bbb;
    resp := 1;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
    resp := 0;
    COMMIT;
  END;
  PROCEDURE buscar(idd in NUMBER, boletas in out CUR_BOL) IS
    bol CUR_BOL;
  BEGIN
    OPEN bol FOR SELECT * FROM BOLETA B INNER JOIN CLIENTE C ON B.RUT_CLIENTE = C.RUT INNER JOIN FORMA_PAGO F ON B.ID_FORMA_PAGO = F.ID_FORM_PAGO INNER JOIN ENTREGA E ON B.ID_ENTREGA = E.ID_ENTREGA WHERE ID_BOLETA = idd;
    boletas := bol;
  END;
  PROCEDURE leer(boletas in out CUR_BOL) IS
    bol CUR_BOL;
  BEGIN
    OPEN bol FOR SELECT * FROM BOLETA B INNER JOIN CLIENTE C ON B.RUT_CLIENTE = C.RUT INNER JOIN FORMA_PAGO F ON B.ID_FORMA_PAGO = F.ID_FORM_PAGO INNER JOIN ENTREGA E ON B.ID_ENTREGA = E.ID_ENTREGA;
    boletas := bol;
  END;
  PROCEDURE ultimo(ult out NUMBER) IS
    u NUMBER;
  BEGIN
    SELECT SUBSTR(ID_BOLETA,INSTR(ID_BOLETA,'-',-1)+1) INTO u FROM BOLETA WHERE ID_BOLETA = (SELECT MAX(ID_BOLETA) FROM BOLETA);
    ult := u;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
    ult := 0;
  END;
